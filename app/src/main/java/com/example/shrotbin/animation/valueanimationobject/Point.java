public class Point {
    private float pointX;
    private float pointY;

    public Point(float x, float y){
        pointX=x;
        pointY=y;
    }

    public float getPointX() {
        return pointX;
    }

    public void setPointX(float pointX) {
        this.pointX = pointX;
    }

    public float getPointY() {
        return pointY;
    }

    public void setPointY(float pointY) {
        this.pointY = pointY;
    }
}
